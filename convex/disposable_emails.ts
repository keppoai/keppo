export const disposableEmailDomains: Set<string> = new Set(DISPOSABLE_EMAIL_DOMAINS);

export const normalizeEmailDomain = (email: string): string | null => {
  const atIndex = email.lastIndexOf("@");
  if (atIndex <= 0 || atIndex >= email.length - 1) {
    return null;
  }

  const domain = email
    .slice(atIndex + 1)
    .trim()
    .toLowerCase();
  if (!domain || domain.includes(" ")) {
    return null;
  }

  return domain;
};

export const isDisposableEmail = (
  email: string,
  options?: { allowDomains?: Iterable<string> },
): boolean => {
  const domain = normalizeEmailDomain(email);
  if (!domain) {
    return false;
  }

  const allowed = new Set<string>();
  if (options?.allowDomains) {
    for (const entry of options.allowDomains) {
      const normalized = entry.trim().toLowerCase();
      if (normalized.length > 0) {
        allowed.add(normalized);
      }
    }
  }

  const segments = domain.split(".");
  for (let index = 0; index < segments.length - 1; index += 1) {
    const candidate = segments.slice(index).join(".");
    if (allowed.has(candidate)) {
      return false;
    }
    if (disposableEmailDomains.has(candidate)) {
      return true;
    }
  }

  return false;
};
